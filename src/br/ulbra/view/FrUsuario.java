    public void readJTableForDesc(String nome, String tipo) throws SQLException {
        DefaultTableModel modelo
                = (DefaultTableModel) tbUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO pdao = new UsuarioDAO();
        for (Usuario p : pdao.readForDesc(nome, tipo)) {
            modelo.addRow(new Object[]{
                p.getIdNome(),
                p.getNome(),
                p.getFone(),
                p.getSenha(),
                p.getEmail(),
                p.getCep(),
                p.getRua(),
                p.getBairro(),
                p.getCidade(),
                p.getEstado(),
                p.getNumero(),});
        }
    }

    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        for (Usuario p : udao.read()) {
            modelo.addRow(new Object[]{
                p.getIdNome(),
                p.getNome(),
                p.getFone(),
                p.getSenha(),
                p.getEmail(),
                p.getCep(),
                p.getRua(),
                p.getBairro(),
                p.getCidade(),
                p.getEstado(),
                p.getNumero(),});
        }
    }

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        controlarBtn(1);
        Usuario u = new Usuario();
        try {
            UsuarioDAO ud = new UsuarioDAO();
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que você deseja excluir este registro?",
                    "Excluir Registro", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                u.setIdNome(Integer.parseInt(edCod.getText()));

                ud.delete(u);
                readJTable();
                controlarBtn(1);
            } else {
                JOptionPane.showMessageDialog(null, "Exclusão Cancelada com sucesso!!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }

    }//GEN-LAST:event_btExcluirActionPerformed

