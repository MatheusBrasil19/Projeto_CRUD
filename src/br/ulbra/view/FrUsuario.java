    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        controlarBtn(1);
        Usuario u = new Usuario();
        try {
            UsuarioDAO ud = new UsuarioDAO();

            u.setNome(edNome.getText());
            u.setEmail(edEmail.getText());
            u.setSenha(edSenha1.getText());
            u.setFone(edFone.getText());
            u.setCep(edCep.getText());
            u.setRua(edRua.getText());
            u.setBairro(edBairro.getText());
            u.setCidade(edCidade.getText());
            u.setEstado(edEstado.getText());
            u.setNumero(Integer.parseInt(edNumero.getText()));

            if (edSenha1.getText().equals(edSenha2.getText())) {
                u.setIdNome(Integer.parseInt(edCod.getText()));
                ud.update(u);
                readJTable();
                controlarBtn(1);
            } else {
                JOptionPane.showMessageDialog(null,
                        "Você inseriu senhas diferentes!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro:" + e.getMessage());
        }

    }//GEN-LAST:event_btAlterarActionPerformed

